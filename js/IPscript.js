// get references to the input and output elements
const textbox = document.getElementById('textbox');
const outputbox = document.getElementById('output');

// get reference to the lookup button
const lookupButton = document.getElementById('lookup');

// add a click event listener to the button
lookupButton.addEventListener('click', function() {
  // get the IP address from the input element
  const ipAddress = textbox.value;

  // make an HTTP GET request to the ipapi.co API
  fetch('http://ip-api.com/line/'+ipAddress+'?fields=message,country,region,city,zip,lat,lon,isp,org,as,asname')
    .then(response => response.text())  // parse the response as JSON
    .then(text => {
      // write the data to the output element
      outputbox.innerHTML = text;

    })
    .catch(error => {
      // if there was an error, write it to the output element
      outputbox.innerHTML = error.message;
    });
});


textbox.addEventListener("focus", () => {
  textbox.style.backgroundColor = "rgb(33, 33, 33)";
});

textbox.addEventListener("blur", () => {
  textbox.style.backgroundColor = "linear-gradient(to right, #f5f7fa, #c3cfe2)";
});



