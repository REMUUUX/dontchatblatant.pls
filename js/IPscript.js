const textbox = document.getElementById("textbox");

textbox.addEventListener("focus", () => {
  textbox.style.backgroundColor = "rgb(33, 33, 33)";
});

textbox.addEventListener("blur", () => {
  textbox.style.backgroundColor = "linear-gradient(to right, #f5f7fa, #c3cfe2)";
});


const textbox2 = document.getElementById("textbox2");

// Get the button element from the DOM
const button = document.getElementById('lookup');

// Add a click event listener to the button
button.addEventListener('click', function() {
  // Initialize a new request to the API page
  const request = new XMLHttpRequest();
  request.open('GET', 'http://ip-api.com/line/'+textbox.value, true);

  // When the request is finished, process the response
  request.onload = function() {
    // Check if the request was successful
    if (request.status >= 200 && request.status < 400) {
      // Get the API response as a string
      const apiResponse = request.responseText;

      // Separate the API response into an array of lines
      const lines = apiResponse.split('\n');
      // Set the textbox's value to the API response
      textbox2.value = lines.join('\n');
    } else {
      // There was an error with the request, log it
      console.error(request.statusText);
    }
  };

  // Send the request
  request.send();
});

